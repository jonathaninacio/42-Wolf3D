/*
** ____________________________________________________________
**                                                             |
**                  destroy_array(char**)                      |
**                                                             |
**                    free a char**                            |
*/

void		destroy_array(char **str)
{
	int a;

	a = 0;
	while (str[a])
	{
		ft_strdel(&str[a]);
		++a;
	}
	free(str);
}
