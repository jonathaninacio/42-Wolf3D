/*
** ____________________________________________________________
**                                                             |
**                     set_kmov(t_data)                        |
**                                                             |
**             set the key press/release to 0				   |
*/

void	set_kmov(t_data *z)
{
	z->kup = 0;
	z->kdown = 0;
	z->kleft = 0;
	z->kright = 0;
}
