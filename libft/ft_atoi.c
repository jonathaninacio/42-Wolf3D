int		ft_atoi(char *str)
{
	int	nb;
	int	z;
	int neg;

	nb = 0;
	z = 0;
	neg = 0;
	while ((str[z] == '\n') || (str[z] == '\t') || (str[z] == '\r') ||
			(str[z] == ' ') || (str[z] == '\v') || (str[z] == '\f'))
		z++;
	if (str[z] == '-')
		neg = 1;
	if ((str[z] == '-') || (str[z] == '+'))
		z++;
	while ((str[z] != '\0') && (str[z] >= '0') && (str[z] <= '9'))
	{
		nb = nb * 10;
		nb = nb + (str[z] - '0');
		z++;
	}
	if (neg == 1)
		nb = -nb;
	return (nb);
}
